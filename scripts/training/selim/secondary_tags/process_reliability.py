def omit_https(url: str) -> str:
    return url.split("/")[-1]


def extract_core_name(name: str) -> str:
    """
    Extract core name from website url
    """
    split_list = name.split(".")

    if len(split_list) <= 2:
        return omit_https(split_list[0])
    if split_list[1] == "com":
        return omit_https(split_list[0])
    else:
        return split_list[1]


def process_reliability(
    entry: str,
    entry_is_url: bool,
    website_to_name_dict: dict = website_to_name_dict,
    name_to_reliability_dict: dict = name_to_reliability_dict,
) -> str:

    if entry_is_url:
        website_name = extract_core_name(entry)
        try:
            core_name = website_to_name_dict[website_name]
        except Exception:
            return "UNKNOWN WEBSITE RELIABILITY"

    else:
        core_name = entry

    try:
        prediction = name_to_reliability_dict[core_name]
    except Exception:
        return "UNKNOWN SOURCE RELIABILITY"

    return prediction
